n = int(input())
e = 0
k = n
n_list = [["*" for _ in range(n)] for _ in range(n)]

while k != 3:
    k /= 3
    e += 1

def recursive(n, e):
    idx = [i for i in range(n) if (i // 3 ** e) % 3 == 1]
    for i in idx:
        for j in idx:
            n_list[i][j] = " "
    if e == 0:
        return
    recursive(n, e-1)

recursive(n, e)

for val in n_list:
    print("".join(val))


